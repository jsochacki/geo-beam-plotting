function [hwgs84_in_m] = get_height_relative_to_WGS84_ellipsoid(latitude, longitude)

   load('1511EGM2008.mat');
   load('1511TOPO.mat');

   hamsl_in_m ...
      = bi_cubic_interpolation1144([latitude, longitude], ...
         ITU_R_1511_TOPO.data, ...
         ITU_R_1511_TOPO.vrows, ...
         ITU_R_1511_TOPO.vcolumns);

   hegm2008_in_m ...
      = bi_cubic_interpolation1144([latitude, longitude], ...
         ITU_R_1511_EGM2008.data, ...
         ITU_R_1511_EGM2008.vrows, ...
         ITU_R_1511_EGM2008.vcolumns);

   hwgs84_in_m = hamsl_in_m + hegm2008_in_m;

end